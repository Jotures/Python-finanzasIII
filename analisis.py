# Definir variables fijas
ingresos = 5000
gastos = 3500

# Calcular ahorro total
ahorro_total = ingresos - gastos

# Calcular margen de ahorro (en porcentaje)
margen_ahorro = (ahorro_total / ingresos) * 100

# Imprimir reporte
print("--- Reporte Financiero ---")
print(f"Ingresos: S/ {ingresos}")
print(f"Gastos: S/ {gastos}")
print(f"Capacidad de Ahorro: {margen_ahorro}%")
